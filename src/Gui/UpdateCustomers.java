    private void Upadate_btn_customerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Upadate_btn_customerActionPerformed
        //update Customer `emp_Number`=?,
        String sql1 ="UPDATE `Customers` SET `fname`=?,`lname`=?,`DOB`=?,`Nationality`=?,`Mobile`=?,`Gender`=?,`image`=?,`Ammount`=?,`Address`=? WHERE Acc_Number=?";
        try {
            InputStream is = new FileInputStream(new File(imagepath1));
            pst1 = conn.prepareStatement(sql1);
            pst1.setString(1, fname_update_cus.getText());
            pst1.setString(2, lname_update_cus.getText());
            pst1.setString(3, dob_update_cus.getText());
            pst1.setString(4, nation_update_cus.getText());
            pst1.setString(5, mobile_update_cus.getText());
            pst1.setString(6, gender_update_cus.getText());
            pst1.setBlob(7, is);
            pst1.setString(8, ammount_update_cus.getText());
            pst1.setString(9, add_update_cus.getText());
            pst1.setString(10, id_update_cus.getText());
            pst1.execute();
            JOptionPane.showMessageDialog(null,"Updated");
            fname_update_cus.setText("");
            lname_update_cus.setText("");
            dob_update_cus.setText("");
            nation_update_cus.setText("");
            mobile_update_cus.setText("");
            gender_update_cus.setText("");
            ammount_update_cus.setText("");
            add_update_cus.setText("");
            //id_update_cus.setText("");
            idimage_cus.setIcon(null);
        } catch (SQLException ex) {
            Logger.getLogger(AdminPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(AdminPanel.class.getName()).log(Level.SEVERE, null, ex);
        }catch (NullPointerException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_Upadate_btn_customerActionPerformed

