    private void search_btn_update_cusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btn_update_cusActionPerformed
        //update_search
        //search the id and set details on the form
        String sql = "select * from Customers where Acc_Number =?";
        String getuserid = id_update_cus.getText();
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, getuserid);
            rs = pst.executeQuery();

            if(rs.next()){
                fname_update_cus.setText(rs.getString("fname"));
                lname_update_cus.setText(rs.getString("lname"));
                ammount_update_cus.setText(rs.getString("Ammount"));
                dob_update_cus.setText(rs.getString("DOB"));
                nation_update_cus.setText(rs.getString("Nationality"));
                mobile_update_cus.setText(rs.getString("Mobile"));
                add_update_cus.setText(rs.getString("Address"));
                gender_update_cus.setText(rs.getString("Gender"));

                //set icon img
                image1 = rs.getBytes("image");
                Image img = Toolkit.getDefaultToolkit().createImage(image1);
                Image imagedResized=img.getScaledInstance(idimage_cus.getWidth(),idimage_cus.getHeight(),Image.SCALE_SMOOTH);
                ImageIcon imageIcon=new ImageIcon(imagedResized);
                idimage_cus.setIcon(imageIcon);
                rs.close();
                pst.close();

            }
            else{
                JDialog dialog = new JDialog();
                dialog.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(dialog, "please enter your correct account number",null,JOptionPane.WARNING_MESSAGE);
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_search_btn_update_cusActionPerformed

